    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id = 0;
        int venta = 0;
        try {
            id = obj.IdCliente(Integer.parseInt(Cedula.getText()));
            if ((Cedula.getText().equals("")) || (Codigo.getText().equals("")) || (auto == 0) || (id == 0)) {
                JOptionPane.showMessageDialog(null, "No pueden aver campos vacios");
            } else {

                if (permiso == true) {
                    obj.InsertarEncVenta(id, Inicio.idempleado, (Codigo.getText()).toUpperCase());

                    if (obj.error == true) {
                        venta = obj.IdVenta((Codigo.getText()).toUpperCase());
                        int cantidad = Integer.parseInt(JOptionPane.showInputDialog(null, "Cuantos carros desea llevar"));
                        obj.InsertarDetVenta(venta, auto, cantidad);
                        auto = 0;
                        permiso = false;
                    }
                } else {
                    venta = obj.IdVenta((Codigo.getText()).toUpperCase());
                    int cantidad = Integer.parseInt(JOptionPane.showInputDialog(null, "Cuantos carros desea llevar"));
                    obj.InsertarDetVenta(venta, auto, cantidad);
                    auto = 0;
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Solo se permiten datos numericos");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

