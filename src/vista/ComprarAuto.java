    private void MarcaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_MarcaItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (this.Marca.getSelectedIndex() > 0) {
                this.Modelo.setModel(new DefaultComboBoxModel(this.rellenaModelos(this.Marca.getSelectedItem().toString())));
            }

            obj.ListaMarcaDetallaAutoComprarMarca(TablaDetalleAuto, (String) Marca.getSelectedItem());
        }
    }//GEN-LAST:event_MarcaItemStateChanged

