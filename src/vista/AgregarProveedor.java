    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ((Nombre.getText().equals("")) || (Direccion.getText().equals("")) || (Telefono.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "No deben aver campos vacios");
        } else {
            agregar.InsertarProveedor((Nombre.getText()).toUpperCase(), (Direccion.getText()).toUpperCase(), Telefono.getText());
            limpieza();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

