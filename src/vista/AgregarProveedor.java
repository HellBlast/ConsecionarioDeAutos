    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        if ((Nombre.getText().equals("")) || (Direccion.getText().equals("")) || (Telefono.getText().equals("")) || (NombreNuevo.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "No deben aver campos vacios");
        } else {
            if (idproveedor == 0) {
                JOptionPane.showMessageDialog(null, "Proveedor no encontrado");
            } else {
                agregar.ModificaProveedor((NombreNuevo.getText()).toUpperCase(), Direccion.getText(), Telefono.getText(), idproveedor);
                limpieza();
            }
        }
    }//GEN-LAST:event_ModificarActionPerformed

