    private void ProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProveedoresMouseClicked
        int seleccion = Proveedores.rowAtPoint(evt.getPoint());
        idproveedor = Integer.parseInt((String.valueOf(Proveedores.getValueAt(seleccion, 0))));
        Nombre.setText(String.valueOf(Proveedores.getValueAt(seleccion, 1)));
        Direccion.setText(String.valueOf(Proveedores.getValueAt(seleccion, 2)));
        Telefono.setText(String.valueOf(Proveedores.getValueAt(seleccion, 3)));


    }//GEN-LAST:event_ProveedoresMouseClicked

