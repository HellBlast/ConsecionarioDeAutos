    private void TablaDetalleAutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDetalleAutoMouseClicked
        int seleccion = TablaDetalleAuto.rowAtPoint(evt.getPoint());
        auto = Integer.parseInt((String.valueOf(TablaDetalleAuto.getValueAt(seleccion, 0))));
        Color.setText(String.valueOf(TablaDetalleAuto.getValueAt(seleccion, 3)));
        Kilometraje.setText(String.valueOf(TablaDetalleAuto.getValueAt(seleccion, 4)));
        Precio.setText(String.valueOf(TablaDetalleAuto.getValueAt(seleccion, 1)));

    }//GEN-LAST:event_TablaDetalleAutoMouseClicked

