    private void ModificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificaActionPerformed

        if ((Color.getText().equals("")) || (Kilometraje.getText().equals("")) || (((String) Marca.getSelectedItem()).equals("SELECCIONAR")) || (Precio.getText().equals("")) || (PrecioNuevo.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "No deben aver campos vacios");
        } else {
            int idPrecio = obj.IdPrecio((Integer.parseInt(Precio.getText())), (Color.getText()).toUpperCase(), Kilometraje.getText(), (String) Modelo.getSelectedItem());
            if (idPrecio == 0) {
                JOptionPane.showMessageDialog(null, "Id del precio no encontrado");
            } else {

                obj.ModificaPrecio(idPrecio);
                obj.InsertarPrecio(Integer.parseInt(PrecioNuevo.getText()), null, auto);

            }


        }    }//GEN-LAST:event_ModificaActionPerformed

